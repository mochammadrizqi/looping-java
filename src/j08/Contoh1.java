
package j08;


public class Contoh1 {
    public static void main(String[] args) {
        //PENGULANGAN FOR
        int x;
        for (x = 1; x <= 5; x++)
        {
           System.out.println("SMK TELKOM Malang"); 
        }
        
        //PENGULANGAN WHILE
        int i = 1;
        while(1<=5)
        {
            System.out.println("SMK TELKOM Malang");
            i++;
        }
        
        //PENGULANGAN DO WHILe
        int j = 1;
        do
        {
            System.out.println("SMK TELKOM Malang");
            j++;
        }
        while (j <=5);
    }
}
